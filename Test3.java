package com.itheima.Test;

public class Test3 {
    public static void main(String[] args) {
        //自增自减
        int a=10;
        int b=++a;//b=11,a=11
        System.out.println(a);
        System.out.println(b);
        int c=5;
        int d=c++;//d=5,c=6
        System.out.println(c);
        System.out.println(d);
    }
}
